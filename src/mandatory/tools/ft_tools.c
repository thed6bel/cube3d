#include "../../../include/cube3d.h"

int	ft_strisspace(const char *str)
{
	if (!str)
		return (0);
	while (*str)
	{
		if (*str != '\f' && *str != '\n' && *str != '\r' && *str != '\t'\
			&& *str != '\v' && *str != ' ')
			return (0);
		str++;
	}
	return (1);
}

